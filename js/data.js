export function pick(arr) { return arr[Math.floor(Math.random() * arr.length)]; }

export function getShuffledCrimes(typeId) {
  const src = (typeId && CRIMES_BY_TYPE[typeId]) ? CRIMES_BY_TYPE[typeId] : CRIMES_BY_TYPE.ex;
  if (!src) return [];
  const arr = [...src];
  // 去重（自定义类型可能返回 null）
  const hot = arr.filter(c => c.hot);
  const cold = arr.filter(c => !c.hot);
  for (let i = hot.length-1; i>0; i--) { const j=Math.floor(Math.random()*(i+1)); [hot[i],hot[j]]=[hot[j],hot[i]]; }
  for (let i = cold.length-1; i>0; i--) { const j=Math.floor(Math.random()*(i+1)); [cold[i],cold[j]]=[cold[j],cold[i]]; }
  return [...hot, ...cold];
}
